function PixelIcon({ map, size = 32, color = '#39FF14', label = '', className = '' }) {
  const rects = map.flatMap((row, y) =>
    row.flatMap((cell, x) =>
      cell
        ? [<rect key={`${x}-${y}`} x={x} y={y} width={1} height={1} fill={color} />]
        : []
    )
  )
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      xmlns="http://www.w3.org/2000/svg"
      role="img"
      aria-label={label}
      className={className}
      style={{ imageRendering: 'pixelated', display: 'block', flexShrink: 0 }}
    >
      {rects}
    </svg>
  )
}

